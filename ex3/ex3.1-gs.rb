# Declaration
puts "Now I will count the eggs:"
# Counts how many eggs. All numbers will be calculated and only the total will be displayed.
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6
# Declaration, no calculations will be performed.
puts "Is it true that 3 + 2 < 5 - 7?"
# Displaying the calculation of the above line.
puts 3 + 2 < 5 - 7
# Declaration, with calculations displayed.
puts "What is 3 + 2? #{3 + 2}"
puts "What is 5 - 7? #{5 - 7}"
# Declaration
puts "Oh, that's why it's false."
